<dt><strong>개인정보의 보유 및 이용기간</strong></dt>
<dd><ol>
<li>귀하의 개인정보는 다음과 같이 개인정보의 수집목적 또는 제공받은 목적이 달성되면 파기됩니다. 단, 상법 등 관련법령의 규정에 의하여 다음과 같이 거래 관련 권리 의무 관계의 확인 등을 이유로 일정기간 보유하여야 할 필요가 있을 경우에는 일정기간 보유합니다.<br />
  <ul><li>회원가입정보 : 회원가입을 탈퇴하거나 회원에서 제명된 경우에 특별한 사유에 사안에 대하여 사전에 보유목적, 기간 및 보유하는 개인정보 항목을 명시하여 동의를 구합니다.</li>
  <li>계약 또는 청약철회 등에 관한 기록 : 5년</li>
  <li>대금결제 및 재화 등의 공급에 관한 기록 : 5년</li>
  <li>소비자의 불만 또는 분쟁처리에 관한 기록 : 3년</li>
  </ul>
</li>
<li>귀하의 동의를 받아 보유하고 있는 거래 정보등을 귀하께서 열람을 요구하는 경우 "본 웹사이트"는 지체 없이 그 열람 · 확인 할 수 있도록 조치합니다.</li>
</ol>
</dd>
