<a name=a2></a>
<dt><strong>개인정보의 수집 및 이용목적</strong></dt>
<dd><ol>
<li>"본 웹사이트"는 다음과 같은 목적을 위하여 개인정보를 수집하고 있습니다. (각 수집항목별 수집목적제시)<br />
  <ul><li>성명, 아이디, 비밀번호 : 회원제 서비스 이용에 따른 본인 식별 절차에 이용</li>
  <li>이메일주소, 전화번호. (뉴스레터 수신여부) : 고지사항 전달, 본인 의사 확인, 불만 처리 등 원활한 의사소통 경로의 확보, 새로운 서비스, 신상품이나 이벤트 정보 등 최신 정보 의 안내</li>
  <li>은행계좌정보, 신용카드정보 : 유료 정보 이용에 대한 요금 결제</li>
  <li>주소, 전화번호 : 청구서, 경품과 쇼핑 물품 배송에 대한 정확한 배송지의 확보</li>
  <li>주민등록번호, 주소 : 인구통계학적분석(이용장의 연령별, 성별, 지역별 통계분석)</li>
  <li>그 외 선택항목 : 개인맞춤 서비스를 제공하기 위한 자료</li>
  </ul>
</li>
<li>단, 이용자의 기본적 인권 침해의 우려가 있는 민감한 개인정보(인종 및 민족, 사상 및 신조, 출신지 및 본적지, 정치적 성향 및 범죄기록, 건강상태 및 성생활 등)는 수집하지 않습니다.</li>
</ol>
</dd>

