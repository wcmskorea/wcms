  </li>
  <li>유료 정보 이용 시 수집하는 개인정보의 범위<br />
    <ul><li>본인확인 정보로써 id와 비밀번호, 성명, 주민등록번호</li>
    <li>결제방법에 따라<br />
      · 거래 은행명, 계좌번호, 거래자 성명<br />
      · 신용카드종류, 카드번호, 유효기한, 비밀번호, 할부기간<br />
      · 휴대폰번호<br />
    </li>
    <li>배송에 필요한 정보로써 보내는 사람과 받는 사람의 성명과 주소, 우편번호, 연락처 등</li>
  </li>
