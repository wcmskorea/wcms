<ol>
  <li>회원가입 시 수집하는 개인정보의 범위<br />
    <ul><li>필수항목 : 희망 id, 비밀번호, 성명, 주민등록번호, 주소, 전화번호, 이메일 주소(뉴스레터 수신여부)</li>
    <li>선택항목 : 회사명, 사업자등록번호, 업태, 종목, 휴대폰번호, 팩스번호, 지로통지</li>
